package submission.domain.common;

public class Form {
	String formIdentifier;
	String formLang;
	String assignCaseNo;
	String submissionNo;
	public String getFormIdentifier() {
		return formIdentifier;
	}
	public void setFormIdentifier(String formIdentifier) {
		this.formIdentifier = formIdentifier;
	}
	public String getFormLang() {
		return formLang;
	}
	public void setFormLang(String formLang) {
		this.formLang = formLang;
	}
	public String getAssignCaseNo() {
		return assignCaseNo;
	}
	public void setAssignCaseNo(String assignCaseNo) {
		this.assignCaseNo = assignCaseNo;
	}
	public String getSubmissionNo() {
		return submissionNo;
	}
	public void setSubmissionNo(String submissionNo) {
		this.submissionNo = submissionNo;
	}
	
}
